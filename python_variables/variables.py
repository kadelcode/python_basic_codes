# Variables are memory locations used to store data values.

_name = 'Willow Winks' # variable _name is given a string value: 'Willow Winks'

number = 23 # assigning 23 to a number variable

grade_point = 'A' # grade_point also a variable


print(_name)

print(number)

print(grade_point)


# Python also allows multiple assignment of variables

# Multiple variables to a single value
u = v = w = 4

#print respective variables
print(u)
print(v)
print(w)

# Multiple variables to multiple values
x, y, z = 10, 20, 30

#print respective variables
print(x)
print(y)
print(z)
